package Week9;

public class ForEachLoop {
    public static void main (String [] args){
        /*
        FASTER WAY TO LOOP AN ARRAY
         */
        int array[] = {2,3,32,47};
        //reading the array using "for-each" loop
        for (int i:array){
            System.out.print(i + " ");
        }



    }
}

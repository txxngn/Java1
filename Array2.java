package Week9;

public class Array2 {
    public static void main (String [] args){

        double price [] = new double[4];    //this array has data type is double
        price [0] = 23.1;                   //"new double" is syntax, mean this array has 4 elements
        price [1] = 12.4;                   //and then we initialize the value for each index of the array
        price [2] = 14.31;
        price [3] = 21.5;

        System.out.println(price[0]);
        System.out.println(price[1]);
        System.out.println(price[2]);
        System.out.println(price[0]);

        int x[] = new int[3];
        x[0] = 2;
        x[1] = 4;
        x[2] = 5;
        System.out.println(x[2]);
        System.out.println(x[1]);




    }
}

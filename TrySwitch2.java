package Switch;

public class TrySwitch2 {
    public static void main (String [] args){
        int number = 2;
        switch (number){
            case 1 :
                System.out.println("one");
                break;
            case 2:
                System.out.println("two");
                break;
            case 3:
                System.out.println("three");
                break;
            default:
                System.out.println("too many!");
                break;

        }




    }
}
